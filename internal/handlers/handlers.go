var Repo *Repository

type Repository struct {
	App *config.AppConfig
	DB  repository.DatabaseRepo
}

func NewRepo(conf *config.AppConfig, db *driver.DB) *Repository {
	return &Repository{
		App: conf,
		DB:  dbrepo.NewPostgresRepo(db.SQL, conf),
	}
}

func NewHandlers(r *Repository) {
	Repo = r
}

